		    <div></div>
		    <div>
		      <p align="justify">	<img src="images/balai_kota.PNG" width="247" height="136"  border width="5" align="left" />Kota Bogor mempunyai sejarah yang panjang dalam Pemerintahan,mengingat sejak zaman Kerajaan Pajajaran sesuai dengan bukti-bukti yang ada seperti dari Prasasti Batu Tulis, nama-nama kampung seperti dikenal dengan nama Lawanggintung, Lawang Saketeng, Jerokuta, Baranangsiang dan Leuwi Sipatahunan diyakini bahwa Pakuan sebagai Ibukota Pajajaran terletak di Kota Bogor.</p>
		      <p align="justify">Pakuan sebagai pusat Pemerintahan Pajajaran terkenal pada pemerintahan Prabu Siliwangi (Sri Baginda Maharaja) yang penobatanya tepat pada tanggal 3 Juni 1482, yang selanjutnya hari tersebut dijadikan hari jadi Bogor, karena sejak tahun 1973 telah ditetapkan oleh DPRD Kabupaten dan Kota Bogor sebagai hari jadi Bogor dan selalu diperingati setiap tahunnya sampai sekarang.<br />
		      <br />
		      Sebagai akibat penyerbuan tentara Banten ke Pakuan Pajajaran catatan mengenai Kota Pakuan tersebut hilang, baru terungkap kembali setelah datangnya rombongan ekspidisi orang-orang Belanda yang dipimpin oleh Scipio dan Riebeck pada tahun 1687, dan mereka meneliti Prasasti Batutulis dan situs-situs lainya yang meyakini bahwa di Bogorlah terletak pusat Pemerintahan Pakuan Pajajaran.<br />
		      <br />
		      Pada tahun 1745 Gubernur Jendral Hindia Belanda pada waktu itu bernama Baron Van Inhoff membangun Istana Bogor, seiring dengan pembangunan jalan Raya Daenless yang menghubungkan Batavia dengan Bogor, sehingga keadaan Bogor mulai bekembang.<br />
		      <br />
		      Pada masa pendudukan Inggris yang menjadi Gubernur Jendralnya adalah Thomas Rafless, beliau cukup berjasa dalam mengembangkan Kota Bogor, dimana Istana Bogor direnofasi dan sebagian tanahnya dijadikan Kebun Raya (Botanikal Garden), beliau juga memperkejakan seorang Planner yang bernama Carsens yang menata Bogor sebagai tempat peristirahatan yang dikenal dengan Buitenzoorg.<br />
		      <br />
		      Setelah Pemerintahan kembali kepada Hindia Belanda pada tahun1903, terbit Undang-undang Desentralisasi yang bertujuan menghapus sistem pemerintahan tradisional diganti dengan sistem administrasi pemerintahan modern sebagai realisasinya dibentuk Staadsgemeente diantaranya adalah.<br />
		      <br />
		      1. Gemeente Batavia ( S. 1903 No.204 ) <br />
		      2. Gemeente Meester Cornelis ( S. 1905 No.206 ) <br />
		      3. Gemeente Buitenzoorg  ( S. 1905 No.208 ) <br />
		      4. Gemeente Bandoeng ( S. 1906 No.121 ) <br />
		      5. Gemeente Cirebon ( S. 1905 No.122 ) <br />
		      6. Gemeente Soekabumi ( S. 1914 No.310 ) <br />
		      <br />
		      (Regeringsalmanak Voor Nederlandsh Indie 1928 : 746-748)<br />
		      <br />
		      Pembentukan Gemeente tersebut bukan untuk kepentingan penduduk Pribumi tetapi untuk kepentingan orang-orang Belanda dan masyarakat Golongan Eropa dan yang dipersamakan (yang menjadi Burgermeester dari Staatsgemeente Buitenzoorg selalu orang-orang Belanda dan baru tahun 1940 diduduki oleh orang Bumiputra yaitu Mr. Soebroto).<br />
		      <br />
		      Pada tahun 1922 sebagai akibat dari ketidakpuasan terhadap peran desentralisasiyang ada maka terbentuklah Bestuursher Voorings Ordonantie atau Undang-undang perubahan tata Pemerintahan Negeri Hindia Belanda (Staatsblad 1922 No. 216), sehinga pada tahun 1992 terbentuklah Regentschaps Ordonantie (Ordonantie Kabupaten) yang membuat ketentuan-ketentuan daerah Otonomi Kabupaten (Staatsblad 1925 No. 79).<br />
		      <br />
		      Propinsi Jawa Barat dibentuk pada tahun 1925 (Staatsblad 1924 No. 378 bij Propince West Java) yang terdiri dari 5 keresidenan, 18 Kabupaten (Regentscape) dan Kotapraja (Staads Gemeente), dimana Buitenzoorg (Bogor) salah satu Staads Gemeente di Propinsi Jawa Barat di bentuk berdasarkan (Staatsblad 1905 No. 208 jo. Staatsblad 1926 No. 368), dengan pripsip Desentralisasi Modern, dimana kedudukan Bugermeester menjadi jelas.<br />
		      <br />
		      Pada masa pendudukan Jepang kedudukan pemerintahan di Kota Bogor menjadi lemah karena pemerintahan dipusatkan pada tingkat keresidenan yang berkedudukan di Kota Bogor, pada masa ini nama-nama lembaga pemerintahan  berubah namanya yaitu: Keresidenan menjadi Syoeoe, Kabupaten/Regenschaps menjadi ken, Kota/Staads Gemeente menjadi Si, Kewedanaan menjadi/Distrik menjadi Gun, Kecamatan/Under Districk menjadi Soe dan desa menjadi Koe.<br />
		      <br />
		      Pada masa setelah kemerdekaan, yaitu setelah pengakuan kedaulatan RI Pemerintahan di Kota Bogor namanya menjadi Kota Besar Bogor yang dibentuk berdasarakan Udang-undang Nomor 16 Tahun 1950.<br />
		      <br />
		      Selanjutnya pada tahun 1957 nama pemerintahan berubah menjadi Kota Praja Bogor, sesuai dengan Undang-undang Nomor. 1Tahun 1957, kemudian dengan Undang-undang Nomor 18 tahun 1965 dan Undang-undang No. 5 Tahun 1974 berubah kembali menjadi Kotamadya Daerah Tingkat II Bogor.<br />
		      <br />
		      Dengan diberlakukanya Undang-undang Nomor 22 Tahun 1999, Kotamadya Daerah Tingkat II Bogor dirubah menjadi Kota Bogor.</p>
